def _validate_scheduling_fields(chore: ChoreCreate):
    """Validate that scheduling fields are appropriate for the frequency type."""
    freq = chore.frequency
    
    if freq == "daily":
        # Daily chores shouldn't have scheduling fields
        pass
    elif freq == "weekly":
        if chore.day_of_week is None:
            raise HTTPException(
                status_code=400, 
                detail="Weekly chores require day_of_week"
            )
    elif freq == "monthly":
        if chore.day_of_week is None or chore.week_of_month is None:
            raise HTTPException(
                status_code=400,
                detail="Monthly chores require day_of_week and week_of_month"
            )
    elif freq == "quarterly":
        if any(x is None for x in [chore.day_of_week, chore.week_of_month, chore.month_of_quarter]):
            raise HTTPException(
                status_code=400,
                detail="Quarterly chores require day_of_week, week_of_month, and month_of_quarter"
            )
    elif freq == "adhoc":
        if chore.scheduled_date is None:
            raise HTTPException(
                status_code=400,
                detail="Ad-hoc chores require scheduled_date"
            )


