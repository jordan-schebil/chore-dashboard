@app.get("/export", tags=["Data"])
def export_data():
    """Export all chores and completions (matches frontend export format)."""
    with get_db() as conn:
        chores_raw = conn.execute(
            """SELECT id, name, frequency, time_of_day as time, minutes,
                      day_of_week as dayOfWeek, week_of_month as weekOfMonth, 
                      month_of_quarter as monthOfQuarter, scheduled_date as scheduledDate
               FROM chores WHERE is_active = 1"""
        ).fetchall()
        
        # Group by frequency (matching frontend structure)
        chores = {"daily": [], "weekly": [], "monthly": [], "quarterly": [], "adhoc": []}
        for c in chores_raw:
            freq = c.pop("frequency")
            # Remove None values
            chores[freq].append({k: v for k, v in c.items() if v is not None})
        
        completions = conn.execute(
            "SELECT chore_id, completed_date FROM completed_tasks"
        ).fetchall()
        
        # Group completions by date (matching frontend structure)
        completed_tasks = {}
        for c in completions:
            date_key = c["completed_date"]
            if date_key not in completed_tasks:
                completed_tasks[date_key] = {}
            completed_tasks[date_key][c["chore_id"]] = True
        
        return {
            "chores": chores,
            "completedTasks": completed_tasks,
            "exportedAt": int(datetime.now().timestamp() * 1000)
        }


@app.post("/import", tags=["Data"])
def import_data(data: dict):
    """Import chores and completions from exported JSON."""
    with get_db() as conn:
        # Clear existing data
        conn.execute("DELETE FROM completed_tasks")
        conn.execute("DELETE FROM chores")
        
        # Import chores
        chores = data.get("chores", {})
        for frequency, chore_list in chores.items():
            for c in chore_list:
                conn.execute(
                    """INSERT INTO chores 
                       (id, name, frequency, time_of_day, minutes,
                        day_of_week, week_of_month, month_of_quarter, scheduled_date)
                       VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)""",
                    (
                        c.get("id", generate_id()),
                        c["name"],
                        frequency,
                        c.get("time", "AM"),
                        c.get("minutes", 10),
                        c.get("dayOfWeek"),
                        c.get("weekOfMonth"),
                        c.get("monthOfQuarter"),
                        c.get("scheduledDate")
                    )
                )
        
        # Import completions
        completed_tasks = data.get("completedTasks", {})
        for date_key, completions in completed_tasks.items():
            for chore_id, is_complete in completions.items():
                if is_complete:
                    try:
                        conn.execute(
                            """INSERT INTO completed_tasks (chore_id, completed_date)
                               VALUES (?, ?)""",
                            (chore_id, date_key)
                        )
                    except sqlite3.IntegrityError:
                        pass  # Skip if chore doesn't exist
        
        return {"status": "imported", "message": "Data imported successfully"}


