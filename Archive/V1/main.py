@app.get("/chores", response_model=list[ChoreResponse], tags=["Chores"])
def list_chores(
    frequency: Optional[FrequencyType] = Query(default=None, description="Filter by frequency"),
    time_of_day: Optional[TimeOfDayType] = Query(default=None, description="Filter by AM/PM"),
    search: Optional[str] = Query(default=None, description="Search chore names")
):
    """
    List all active chores with optional filters.
    Used by the Manage tab in the frontend.
    """
    with get_db() as conn:
        query = """
            SELECT id, name, frequency, time_of_day, minutes,
                   day_of_week, week_of_month, month_of_quarter, 
                   scheduled_date, created_at, updated_at
            FROM chores
            WHERE is_active = 1
        """
        params = []
        
        if frequency:
            query += " AND frequency = ?"
            params.append(frequency)
        
        if time_of_day:
            query += " AND time_of_day = ?"
            params.append(time_of_day)
        
        if search:
            query += " AND name LIKE ?"
            params.append(f"%{search}%")
        
        query += """
            ORDER BY 
                (SELECT sort_order FROM frequencies WHERE id = frequency),
                time_of_day,
                name
        """
        
        return conn.execute(query, params).fetchall()


@app.get("/chores/{chore_id}", response_model=ChoreResponse, tags=["Chores"])
def get_chore(chore_id: str):
    """Get a single chore by ID."""
    with get_db() as conn:
        chore = conn.execute(
            """SELECT id, name, frequency, time_of_day, minutes,
                      day_of_week, week_of_month, month_of_quarter,
                      scheduled_date, created_at, updated_at
               FROM chores WHERE id = ? AND is_active = 1""",
            (chore_id,)
        ).fetchone()
        
        if not chore:
            raise HTTPException(status_code=404, detail="Chore not found")
        
        return chore


@app.post("/chores", response_model=ChoreResponse, status_code=201, tags=["Chores"])
def create_chore(chore: ChoreCreate):
    """
    Create a new chore.
    Validates that scheduling fields match the frequency type.
    """
    # Validate scheduling fields based on frequency
    _validate_scheduling_fields(chore)
    
    chore_id = generate_id()
    now = datetime.now().isoformat()
    
    with get_db() as conn:
        conn.execute(
            """INSERT INTO chores 
               (id, name, frequency, time_of_day, minutes, 
                day_of_week, week_of_month, month_of_quarter, 
                scheduled_date, created_at, updated_at)
               VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)""",
            (
                chore_id,
                chore.name,
                chore.frequency,
                chore.time_of_day,
                chore.minutes,
                chore.day_of_week,
                chore.week_of_month,
                chore.month_of_quarter,
                chore.scheduled_date.isoformat() if chore.scheduled_date else None,
                now,
                now
            )
        )
        
        return {
            "id": chore_id,
            **chore.model_dump(),
            "scheduled_date": chore.scheduled_date.isoformat() if chore.scheduled_date else None,
            "created_at": now,
            "updated_at": now
        }


@app.put("/chores/{chore_id}", response_model=ChoreResponse, tags=["Chores"])
def update_chore(chore_id: str, chore: ChoreUpdate):
    """
    Update an existing chore.
    Only provided fields are updated (PATCH-like behavior).
    """
    with get_db() as conn:
        # Get existing chore
        existing = conn.execute(
            "SELECT * FROM chores WHERE id = ? AND is_active = 1",
            (chore_id,)
        ).fetchone()
        
        if not existing:
            raise HTTPException(status_code=404, detail="Chore not found")
        
        # Merge updates with existing data
        update_data = chore.model_dump(exclude_unset=True)
        
        if not update_data:
            raise HTTPException(status_code=400, detail="No fields to update")
        
        # Build dynamic UPDATE query
        set_clauses = []
        params = []
        for field, value in update_data.items():
            set_clauses.append(f"{field} = ?")
            if field == "scheduled_date" and value:
                params.append(value.isoformat())
            else:
                params.append(value)
        
        params.append(chore_id)
        
        conn.execute(
            f"UPDATE chores SET {', '.join(set_clauses)} WHERE id = ?",
            params
        )
        
        # Return updated chore
        return conn.execute(
            """SELECT id, name, frequency, time_of_day, minutes,
                      day_of_week, week_of_month, month_of_quarter,
                      scheduled_date, created_at, updated_at
               FROM chores WHERE id = ?""",
            (chore_id,)
        ).fetchone()


@app.delete("/chores/{chore_id}", status_code=204, tags=["Chores"])
def delete_chore(chore_id: str):
    """
    Soft delete a chore (sets is_active = 0).
    Preserves completion history.
    """
    with get_db() as conn:
        result = conn.execute(
            "UPDATE chores SET is_active = 0 WHERE id = ? AND is_active = 1",
            (chore_id,)
        )
        
        if result.rowcount == 0:
            raise HTTPException(status_code=404, detail="Chore not found")


