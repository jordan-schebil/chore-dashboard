@app.get("/chores/date/{target_date}", response_model=DateChoresResponse, tags=["Calendar"])
def get_chores_for_date(target_date: date):
    """
    Get all chores scheduled for a specific date.
    This powers the calendar day view - returns chores grouped by AM/PM
    with their completion status.
    """
    # Calculate scheduling values for the date
    # Note: Python weekday() is 0=Mon, but JS/our schema uses 0=Sun
    day_of_week = (target_date.weekday() + 1) % 7
    week_of_month = (target_date.day - 1) // 7 + 1
    month_of_quarter = (target_date.month - 1) % 3 + 1
    date_str = target_date.isoformat()
    
    with get_db() as conn:
        # Get chores for this date
        chores = conn.execute(
            """
            SELECT 
                c.id,
                c.name, 
                c.frequency,
                c.time_of_day,
                c.minutes,
                CASE WHEN ct.chore_id IS NOT NULL THEN 1 ELSE 0 END as is_completed
            FROM chores c
            LEFT JOIN completed_tasks ct 
                ON c.id = ct.chore_id AND ct.completed_date = ?
            WHERE c.is_active = 1
              AND (
                c.frequency = 'daily'
                OR (c.frequency = 'weekly' AND c.day_of_week = ?)
                OR (c.frequency = 'monthly' AND c.week_of_month = ? AND c.day_of_week = ?)
                OR (c.frequency = 'quarterly' AND c.month_of_quarter = ? AND c.week_of_month = ? AND c.day_of_week = ?)
                OR (c.frequency = 'adhoc' AND c.scheduled_date = ?)
              )
            ORDER BY c.time_of_day, c.name
            """,
            (date_str, day_of_week, week_of_month, day_of_week,
             month_of_quarter, week_of_month, day_of_week, date_str)
        ).fetchall()
        
        # Calculate summary
        total_count = len(chores)
        completed_count = sum(1 for c in chores if c["is_completed"])
        total_minutes = sum(c["minutes"] for c in chores)
        completed_minutes = sum(c["minutes"] for c in chores if c["is_completed"])
        
        # Convert is_completed to boolean for JSON
        for chore in chores:
            chore["is_completed"] = bool(chore["is_completed"])
        
        return {
            "date": target_date,
            "chores": chores,
            "summary": {
                "total_count": total_count,
                "completed_count": completed_count,
                "remaining_count": total_count - completed_count,
                "total_minutes": total_minutes,
                "completed_minutes": completed_minutes,
                "remaining_minutes": total_minutes - completed_minutes,
                "progress_percent": round((completed_count / total_count) * 100) if total_count > 0 else 0
            }
        }


@app.get("/calendar/month/{year}/{month}", tags=["Calendar"])
def get_month_summary(year: int, month: int = Path(..., ge=1, le=12)):
    """
    Get summary data for an entire month (for calendar heatmap).
    Returns count and minutes for each day.
    """
    from calendar import monthrange
    
    _, days_in_month = monthrange(year, month)
    
    results = {}
    for day in range(1, days_in_month + 1):
        target_date = date(year, month, day)
        day_data = get_chores_for_date(target_date)
        results[target_date.isoformat()] = day_data["summary"]
    
    return results


