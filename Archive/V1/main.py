@app.post("/completions", response_model=CompletionResponse, status_code=201, tags=["Completions"])
def mark_complete(completion: CompletionCreate):
    """Mark a chore as complete for a specific date."""
    with get_db() as conn:
        # Verify chore exists
        chore = conn.execute(
            "SELECT id FROM chores WHERE id = ? AND is_active = 1",
            (completion.chore_id,)
        ).fetchone()
        
        if not chore:
            raise HTTPException(status_code=404, detail="Chore not found")
        
        try:
            now = datetime.now().isoformat()
            conn.execute(
                """INSERT INTO completed_tasks (chore_id, completed_date, completed_at)
                   VALUES (?, ?, ?)""",
                (completion.chore_id, completion.completed_date.isoformat(), now)
            )
            
            return {
                "chore_id": completion.chore_id,
                "completed_date": completion.completed_date,
                "completed_at": now
            }
        except sqlite3.IntegrityError:
            raise HTTPException(
                status_code=409, 
                detail="Chore already marked complete for this date"
            )


@app.delete("/completions/{chore_id}/{completed_date}", status_code=204, tags=["Completions"])
def unmark_complete(chore_id: str, completed_date: date):
    """Remove completion status for a chore on a specific date."""
    with get_db() as conn:
        result = conn.execute(
            "DELETE FROM completed_tasks WHERE chore_id = ? AND completed_date = ?",
            (chore_id, completed_date.isoformat())
        )
        
        if result.rowcount == 0:
            raise HTTPException(status_code=404, detail="Completion record not found")


@app.post("/completions/toggle", tags=["Completions"])
def toggle_completion(completion: CompletionCreate):
    """
    Toggle completion status (convenience endpoint for the frontend).
    Returns the new state.
    """
    with get_db() as conn:
        # Check if already completed
        existing = conn.execute(
            "SELECT 1 FROM completed_tasks WHERE chore_id = ? AND completed_date = ?",
            (completion.chore_id, completion.completed_date.isoformat())
        ).fetchone()
        
        if existing:
            # Remove completion
            conn.execute(
                "DELETE FROM completed_tasks WHERE chore_id = ? AND completed_date = ?",
                (completion.chore_id, completion.completed_date.isoformat())
            )
            return {"chore_id": completion.chore_id, "is_completed": False}
        else:
            # Add completion
            now = datetime.now().isoformat()
            conn.execute(
                """INSERT INTO completed_tasks (chore_id, completed_date, completed_at)
                   VALUES (?, ?, ?)""",
                (completion.chore_id, completion.completed_date.isoformat(), now)
            )
            return {"chore_id": completion.chore_id, "is_completed": True}


