FrequencyType = Literal["daily", "weekly", "monthly", "quarterly", "adhoc"]
TimeOfDayType = Literal["AM", "PM"]


class ChoreBase(BaseModel):
    """Base chore fields for create/update."""
    name: str = Field(..., min_length=1, max_length=200)
    frequency: FrequencyType
    time_of_day: TimeOfDayType = "AM"
    minutes: int = Field(default=10, gt=0, le=480)
    day_of_week: Optional[int] = Field(default=None, ge=0, le=6)
    week_of_month: Optional[int] = Field(default=None, ge=1, le=4)
    month_of_quarter: Optional[int] = Field(default=None, ge=1, le=3)
    scheduled_date: Optional[date] = None


class ChoreCreate(ChoreBase):
    """Schema for creating a new chore."""
    pass


class ChoreUpdate(BaseModel):
    """Schema for updating a chore (all fields optional)."""
    name: Optional[str] = Field(default=None, min_length=1, max_length=200)
    frequency: Optional[FrequencyType] = None
    time_of_day: Optional[TimeOfDayType] = None
    minutes: Optional[int] = Field(default=None, gt=0, le=480)
    day_of_week: Optional[int] = Field(default=None, ge=0, le=6)
    week_of_month: Optional[int] = Field(default=None, ge=1, le=4)
    month_of_quarter: Optional[int] = Field(default=None, ge=1, le=3)
    scheduled_date: Optional[date] = None


class ChoreResponse(ChoreBase):
    """Schema for chore responses."""
    id: str
    created_at: str
    updated_at: str
    
    class Config:
        from_attributes = True


class CompletionCreate(BaseModel):
    """Schema for marking a chore complete."""
    chore_id: str
    completed_date: date


class CompletionResponse(BaseModel):
    """Schema for completion responses."""
    chore_id: str
    completed_date: date
    completed_at: str


class DateChoresResponse(BaseModel):
    """Schema for chores on a specific date with completion status."""
    date: date
    chores: list[dict]
    summary: dict


class BulkCompletionStatus(BaseModel):
    """Schema for getting completion status for multiple chores."""
    date: date
    completions: dict[str, bool]  # chore_id -> is_completed


